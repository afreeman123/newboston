class apples {
  public static void main(String args[]){
    //Math class has various calculating methods within it

    //Math.abs() finds the absoulute value of the parameter given
    System.out.println(Math.abs(-26.7));

    //Math.ceil() rounds up
    System.out.println(Math.ceil(7.4));

    //Math.floor() rounds down
    System.out.println(Math.floor(5.6));

    //Math.max() takes multiple parameters and finds the max of the those numbers
    System.out.println(Math.max(8.4,5.2));

    //Math.min() takes multiple parameters and finds the min of the those numbers
    System.out.println(Math.min(8.4,5.2));

    //Math.pow(x,y) takes two parameters and calculates x^y
    System.out.println(Math.pow(5,3));

    //Math.sqrt() find the square root of the value given
    System.out.println(Math.sqrt(9));
  }
}
